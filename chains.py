def similarity_match(input_chain, input_drink, top_similar=10):
    tags = None

    # 🔍 Find selected drink tags
    for series_name, drinks in chains[input_chain].items():
        for drink in drinks:
            if drink["name"] == input_drink:
                tags = set(drink["tags"])
                break

    if not tags:
        return []

    top_recommendations = []

    for chain_name, series in chains.items():
        for series_name, drinks in series.items():
            for drink in drinks:

                # Skip exact same drink
                if drink["name"] == input_drink:
                    continue

                boba_tags = set(drink["tags"])
                intersection = tags & boba_tags
                union = tags | boba_tags

                if union:  # avoid division by zero
                    similarity_percentage = int((len(intersection) / len(union)) * 100)
                else:
                    similarity_percentage = 0

                if similarity_percentage > 0:
                    top_recommendations.append({
                        "name": drink["name"],
                        "chain": chain_name,
                        "series": series_name,
                        "similarity": similarity_percentage
                    })

    # Sort by similarity
    top_recommendations.sort(
        key=lambda item: item["similarity"],
        reverse=True
    )

    return top_recommendations[:top_similar]
